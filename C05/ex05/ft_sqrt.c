int	ft_sqrt(int nb)
{
	int	i;
	int	square_root_max;

	i = 0;
	square_root_max = 46341;
	while (i < square_root_max)
	{
		if (i * i == nb)
			return (i);
		else
			i++;
	}
	return (0);
}
